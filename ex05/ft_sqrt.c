#include <unistd.h>
#include <stdio.h>
int ft_sqrt(int nb){
	int i;
	int j;

	i=0;
	j=0;
		while (i <= (nb/2))
		{		
			j=i*i;
			if (j == nb)
			{
				return i;
			}	
			i++;
			if ( (j != nb) && (i > (nb/2) ) )
			{
				return 0;		
			}
		}
return j;
}
